var date = new Date()

console.log(date.getDate()); // 14
console.log(date.getMonth()); // 0
console.log(date.getFullYear()); // 2023

console.log(date.getHours()); // 16
console.log(date.getMinutes()); // 39

console.log(date.toTimeString()); // 16:39:53 GMT+0600 (Bangladesh Standard Time)
console.log(date.toDateString()); // Sat Jan 14 2023
console.log(date.toLocaleDateString());  // 1/14/2023







